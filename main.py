def is_palindrome(s: str) -> bool:
    # Пошаговый алгоритм действий:
    # 1) Привести строку к нижнему регистру и удалить все символы, которые не являются буквами или цифрами (нормализация).
    #    Это обеспечивает нечёткое сравнение: игнорируем регистр и посторонние символы.
    # 2) Инициализировать два указателя:
    #    i = 0 (начало строки) и j = len(norm) - 1 (конец строки).
    # 3) Пока i < j:
    #    a) Если символы norm[i] и norm[j] не совпадают, вернуть False — строка не палиндром.
    #    b) Увеличить i на 1 и уменьшить j на 1 (перемещаем указатели к центру).
    # 4) Если цикл завершён без несовпадений, вернуть True — строка палиндром.
    norm = ''.join(ch.lower() for ch in s if ch.isalnum())
    i, j = 0, len(norm) - 1
    while i < j:
        if norm[i] != norm[j]:
            return False
        i += 1
        j -= 1
    return True
print(is_palindrome("a rosa upala na lapu asor"))